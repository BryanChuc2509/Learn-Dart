// Los operadores de comparaciÃ³n nos permiten comparar dos expresiones y tener un resultado booleano

void main () {

  // Igual a : ==
  bool expresion = 2 == 2;
  print(expresion); //true

  // Diferente a: !=
  expresion = 2 != 3; //true
  print(expresion);

  // Mayor que : >
  expresion = 10 > 5;
  print(expresion); //true

  // Menor que : <
  expresion = 1 < 2;
  print(expresion); // true

  // Mayor o igual a : >=
  expresion = 10 >= 10;
  print(expresion); //true

  // Menor o igual a: <= 
  expresion = 2 <= 2;
  print(expresion); //true 
}